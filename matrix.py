A = [
    [1, 2, 3, 4, 5],
    [5, 4, 3, 2, 1],
    [1, 3, 5, 7, 9],
    [9, 7, 5, 3, 1],
    [2, 4, 6, 8, 0]
]

B = [
    [0, 1, 2, 3, 4],
    [4, 3, 2, 1, 0],
    [1, 0, 1, 0, 1],
    [1, 2, 3, 4, 5],
    [5, 4, 3, 2, 1]
]

C = [[0 for _ in range(5)] for _ in range(5)]

for i in range(5):
    for j in range(5):
        for k in range(5):
            C[i][j] += A[i][k] * B[k][j]

